import heapq
import matplotlib.pyplot as plt
import numpy as np

def visualize_path_matplotlib(maze, path):
    maze_visual = np.array(maze)

    # Mark the path in the maze
    for x, y in path:
        maze_visual[x][y] = 2  # Mark path with "2"

    # Create a color map
    cmap = plt.cm.get_cmap('viridis', 3)  # 3 colors: 0 (open), 1 (blocked), 2 (path)
    plt.imshow(maze_visual, cmap=cmap)
    plt.colorbar(ticks=[0, 1, 2], label="Legend")
    plt.title("Maze Path Visualization")
    plt.show()

def aStarSearch(maze, start, goal):
    # directions the robot can move towards: up, down, left, right
    direction = [(-1, 0), (1, 0), (0, -1), (0, 1)]
    
    # priority queue for nodes to be visited and pushing the start node into the queue
    toBeVisited = []
    heapq.heappush(toBeVisited, (0, start))
    
    # visited nodes and their parents
    alreadyVisited = {}
    
    # f = g + h
    # h = heuristic value of the current node
    # g = cost to reach the current node
    # f = total cost
    g = {start: 0}
    f = {start: heuristicList[start[0]][start[1]]}
    
    while toBeVisited:
        # popping the node with the smallest f value
        _, current = heapq.heappop(toBeVisited)
        print(f"Current Node: {current}")
        
        # if goal is reached, return the path
        if current == goal:
            print("Goal reached!")
            return reconstructPath(alreadyVisited, current)
        
        # exploring neighbors
        for x, y in direction:
            neighbor = (current[0] + x, current[1] + y)
            
            # checking if the neighbor is within bounds and not a wall
            if (0 <= neighbor[0] < len(maze) and 0 <= neighbor[1] < len(maze[0]) and maze[neighbor[0]][neighbor[1]] == 0):
                
                # adding the cost of the current node from start
                newG = g[current] + 1
                
                # if this path is better, update scores and add to queue
                if neighbor not in g or newG < g[neighbor]:
                    alreadyVisited[neighbor] = current
                    g[neighbor] = newG
                    f[neighbor] = newG + heuristicList[neighbor[0]][neighbor[1]]
                    
                    # adding the neighbor to the queue if not already in it
                    if neighbor not in [n[1] for n in toBeVisited]:
                        heapq.heappush(toBeVisited, (f[neighbor], neighbor))
    
    print("No path exists.")
    return None

def reconstructPath(alreadyVisited, current):
    path = [current]
    while current in alreadyVisited:
        current = alreadyVisited[current]
        path.append(current)
    path.reverse()
    return path

